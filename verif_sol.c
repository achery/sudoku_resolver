#include "function.h"

int		verif_sol(char **tab)
{
	int	i;
	int	j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			if (tab[i][j] == '.')
			{
				return (0);
			}
			j = j + 1;
		}
		i = i + 1;
	}
	return (1);
}
